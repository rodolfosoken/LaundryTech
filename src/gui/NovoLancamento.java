    public void soma() {
        float sum = 0;
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            sum += (Float.parseFloat(String.valueOf(jTable1.getValueAt(i, 2))) * Float.parseFloat(String.valueOf(jTable1.getValueAt(i, 1))));
        }

        Subtotal.setText(String.valueOf(sum));

    }

